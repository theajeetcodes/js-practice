// ======= JavaScript Intro =======
// JS is a programming language for making websites interactive.
// It works in browsers and on servers (Node.js).
// Dynamic, loosely typed, and multi-paradigm.
// Example:
console.log("Hello World! Welcome to JavaScript");


// =================================================
//              JavaScript Variables
// =================================================

// THEORY:
// Variables are containers used to store data in JavaScript.
// We create variables when we want to save information like:
// - numbers
// - text (strings)
// - booleans (true/false)
// - etc.
//
// JavaScript provides 3 ways to declare variables:
// 1. let     -->  for values that can change 
// 2. const   -->  for values that cannot change 
// 3. var     -->  old method, avoid using in modern JavaScript

// ==================================
//     RULES FOR NAMING VARIABLES
// ==================================
// 1. Variable names cannot start with a number
// 2. Variable names cannot contain spaces 
// 3. Variable names cannot use JavaScript reserved keywords (like let, if, for)
// 4. Use camelCase for professional coding style

// ================================
//           EXAMPLES
// ================================

// Example 1: Storing a number 
let price = 99.99;
console.log("Price:",price);


// Example 2: Storing text (string)
let userName = "Ajeet Yadav";
console.log("User Name:",userName);


// Example 3: Using const (fixed value)
const PI = 3.14;
console.log("PI Value:",PI);


// Example 4: Updating variable
let age = 20;
console.log("Before update:",age);
age = 21;   // value updated
console.log("After update:",age);


